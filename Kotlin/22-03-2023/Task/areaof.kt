fun main()
{
    // area of circle
    var pi = 3.14
    var r = 3.4

    var radius = pi*r*r

    if (radius>0)
    {
        println("Area of circle: $radius")
    }

    // area of triangle
    var b = 4
    var h = 13

    var triangleArea = (b*h)/2

    if(triangleArea>0)
    {
        println("Area of triangle: $triangleArea")
    }

    // area of rectangle

    var w = 14
    var l = 10

    var rectangleArea = w*l

    if (rectangleArea>0)
    {
        println("Area of rectangle: $rectangleArea")
    }
}